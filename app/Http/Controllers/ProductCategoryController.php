    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            "name" => "required",
        ]);
        $poductCategory=new ProductCategory();
        $poductCategory->name=$request->name;
        $poductCategory->save();

        return redirect()->route("product_category.create")->with("toast","Product Category Add Successful");
    }

    /**
